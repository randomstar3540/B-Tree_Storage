void print_tree(struct node * list, uint64_t num){
    for (int i = 0; i < num; i++){
        int num_of_keys = (list + i)->num_keys;
        printf("NODE: ");
        for (int j = 0; j < num_of_keys; ++j) {
            printf(" %d ", list[i].keys[j]);
        }
        printf("\n");
    }
    printf("\n");
}

static int compare_file_info(char * filename){

    //open files
    FILE * expected = fopen(filename,"r");
    FILE * output = fopen("test/out","r");
    if (expected == NULL || output == NULL){
        fclose(expected);
        fclose(output);
        return -1;
    }

    //compare size
    fseek(expected, 0L, SEEK_END);
    int64_t expected_size = ftell(expected);
    fseek(expected, 0L, SEEK_SET);

    fseek(output, 0L, SEEK_END);
    int64_t output_size = ftell(output);
    fseek(output, 0L, SEEK_SET);

    if(expected_size != output_size){
        fclose(expected);
        fclose(output);
        return -1;
    }

    int e;//expected
    int o;//output
    while ((e = getc(expected)) != EOF && (o = getc(output)) != EOF){
        if(e != o){
            fclose(expected);
            fclose(output);
            return -1;
        }
    }
    fclose(expected);
    fclose(output);
    return 0;
}

void free_list(struct node * list, uint64_t num){
    for (int i = 0; i < num; i++){
        free((list + i)->keys);
    }
    free(list);
}

